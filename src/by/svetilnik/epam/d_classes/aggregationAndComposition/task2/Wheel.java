package by.svetilnik.epam.d_classes.aggregationAndComposition.task2;

public class Wheel {
    private String mark;
    private int diameter;

    public Wheel(String mark, int diameter) {
        this.mark = mark;
        this.diameter = diameter;
    }

    public String getMark() {
        return mark;
    }

    public int getDiameter() {
        return diameter;
    }

}
